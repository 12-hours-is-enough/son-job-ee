function toggleDetail(btn) {
    let detail = btn.parentNode.nextElementSibling;
    if (detail.style.display === "block") {
        detail.style.display = "none";
        btn.textContent = "▼";
    } else {
        detail.style.display = "block";
        btn.textContent = "▲";
    }
}

